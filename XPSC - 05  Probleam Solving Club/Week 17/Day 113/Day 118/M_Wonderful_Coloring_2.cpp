void solve()
{

    string word;
    cin >> word;

    int N = word.size();

    vector<int> freq(256,0);

    for(char val : word)
    {
        freq[val]++;
        if(freq[val]>2)
        {
            N--;
        }
    }

    cout << N/2 << endl;

}

